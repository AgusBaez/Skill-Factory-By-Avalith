// --- Condicionales ---
// if
if (4 === 8) {
  /* no entra */
} else {
  // Al no cumplirse la condición del if se saltea el bloque de codigo y se corre el resto, pero en el caso de querer tener un bloque para CUANDO NO se cumple la condición se usa el else
}
// A través del operador if podemos generar bloques de código que se solo entra si se cumple una condición que devuelva un Booleano

// --- Condicional ---
// while
// A través del operador while podemos generar bloques de código que se repite si se cumple una condición que devuelva un Booleano
while (i < 10) {
  // se repite mientra la variable i sea menor que 10
}
// Do While
// Do while es una forma de hacer un bloque while con la unica diferencia es que siempre entra por una vez antes de checkear la condición

// -- Iterador ---
// for
// A través del operador for podemos generar bloques de código que se repite una cantidad especificada de veces con un contador
for (let i = 0; i < 10; i++) {
  // se repite mientra la variable i sea menor que 10, i arranca en 0 e i aumenta su valor por 1 cada iteracion
}

