class Calculator {
  sum(num1, num2) {
    return num1 + num2;
  }
  subtract(num1, num2) {
    return num1 - num2;
  }
  dividir(num1, num2) {
    return num1 / num2;
  }
  split(num1, num2) {
    return num1 * num2;
  }
  module(num1, num2) {
    return num1 % num2;
  }
}
